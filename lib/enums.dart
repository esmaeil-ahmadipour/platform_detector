enum PlatformName {
  android, // usage: when target platform is android .
  web_android, // usage: when target platform is android and run on web .
  iOS, // usage: when target platform is iOS.
  web_iOS, // usage: when target platform is iOS and run on web .
  macOs, // usage: when target platform is macOs .
  web_macOs, // usage: when target platform is macOs and run on web .
  windows, // usage: when target platform is windows .
  web_windows, // usage: when target platform is windows and run on web .
  linux, // usage: when target platform is linux .
  web_linux // usage: when target platform is linux and run on web .
}

enum PlatformType {
  mobile, // usage: when application running on mobile .
  desktop, // usage: when application running on desktop .
  web // usage: when application running on web .
}

enum PlatformCompany {
  apple, // usage: when target platform is : iOS , macOs .
  google, // usage: when target platform is : android .
  linux, // usage: when target platform is : linux .
  microsoft // usage: when target platform is : windows .
}
